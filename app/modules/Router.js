/* required classes */
const Cache = require("./Cache.js");

/* helpers */
const cache = new Cache();

/* @-<Router *****************************************************************/
/*****************************************************************************/
class Router
{
  /* @@-<constructor *********************************************************/
  /***************************************************************************/
  constructor()
  {}

  /* @@navigate **************************************************************/
  /***************************************************************************/
  navigate(request)
  {
    if(request === "/") // index
    {
      return cache.html["index"];
    }
  }
}

/* Export */
module.exports = Router;